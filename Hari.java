    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
    if (tahun.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Tahun harus diisi!");
        return;
    }

    try {
        int tahunValue = Integer.parseInt(tahun.getText().trim());
        String bulanValue = bulan.getSelectedItem().toString();
        int jumlahHari;
        
        // Proses perhitungan hari
        switch(bulanValue) {
            case "Januari":
            case "Maret":
            case "Mei":
            case "Juli":
            case "Agustus":
            case "Oktober":
            case "Desember":
                jumlahHari = 31;
                break;
                
            case "April":
            case "Juni":
            case "September":
            case "November":
                jumlahHari = 30;
                break;
                
                //kabisat
            case "Febuari":
                if ((tahunValue % 400 == 0) || ((tahunValue % 4 == 0) && (tahunValue % 100 != 0))) {
                    jumlahHari = 29;
                } else {
                    jumlahHari = 28;
                }
                break;
                
            default:
                jumlahHari = 0;
                break;
        }
        
//        jLabel3.setText("Jumlah hari: " + bulanValue + " tahun "+ tahunValue + " adalah " + jumlahHari + " hari");
        jLabel3.setText("Jumlah hari: " + jumlahHari + " hari");
                       
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka");
        tahun.setText("");
        tahun.requestFocus();
        }
    }//GEN-LAST:event_hitungActionPerformed
    


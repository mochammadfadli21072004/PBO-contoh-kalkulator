    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
         if (!jLabel3.getText().isEmpty()) {
        StringBuilder hasil = new StringBuilder();
        hasil.append("----------------------------------------\n");
        hasil.append("Tanggal Perhitungan: ").append(java.time.LocalDateTime.now()).append("\n");
        hasil.append("Tahun: ").append(tahun.getText()).append("\n");
        hasil.append("Bulan: ").append(bulan.getSelectedItem().toString()).append("\n");
        hasil.append(jLabel3.getText()).append("\n");
        hasil.append("----------------------------------------");
        
        JOptionPane.showMessageDialog(this, hasil.toString());
    } else {
        JOptionPane.showMessageDialog(this, "Lakukan perhitungan terlebih dahulu sebelum menyimpan!");
    }
    }//GEN-LAST:event_simpanActionPerformed


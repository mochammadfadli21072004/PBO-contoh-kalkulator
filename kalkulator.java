    private void showNum(String num) {
        angka += num;
        output.setText(angka);
    }
    
    private void opsiSelect(String op) {
        bil1 = Double.parseDouble(angka);
        angka = "";
        operator = op;
        output.setText(op);
    }
    
    private void hitungHasil() {
        bil2 = Double.parseDouble(angka);
        switch (operator) {
            case "+":
                hasil = bil1 + bil2;
                break;
            case "-":
                hasil = bil1 - bil2;
                break;
            case "*":
                hasil = bil1 * bil2;
                break;
            case "/":
                if (bil2 != 0) {
                    hasil = bil1 / bil2;
                } else {
                    output.setText("tidak bisa dibagi 0");
                    return;
                }
                break;
            case "%":
                hasil = bil1 % bil2;
                break;
            default:
                output.setText("Error");
                return;
        }
        output.setText(String.valueOf(hasil));
        angka = String.valueOf(hasil); 
    }
    
    private void resetCalculator() {
        angka = "";
        bil1 = 0;
        bil2 = 0;
        hasil = 0;
        operator = "";
        output.setText("");
    }
    
    private void deleteLastCharacter() {
        if (angka.length() > 0) {
            angka = angka.substring(0, angka.length() - 1);
            output.setText(angka);
        }
    }

    private void calculateSquare() {
        if (!angka.isEmpty()) {
            bil1 = Double.parseDouble(angka);
            hasil = bil1 * bil1;
            output.setText(String.valueOf(hasil));
            angka = String.valueOf(hasil);
        }
    }
